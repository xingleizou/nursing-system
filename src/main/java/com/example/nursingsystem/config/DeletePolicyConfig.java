package com.example.nursingsystem.config;

/**
 * 数据删除策略配置说明
 * 
 * 本类用于说明护理系统中各业务表的删除策略选择
 * 
 * 删除策略选择原则:
 * 1. 逻辑删除 - 适用于需要保留历史记录、可恢复、审计要求的业务数据
 * 2. 物理删除 - 适用于临时数据、垃圾数据、隐私数据、大数据量场景
 * 
 * @author Nursing System Team
 * @version 1.0
 */
public class DeletePolicyConfig {
    
    // ==================== 逻辑删除的表 (使用 @TableLogic) ====================
    
    /**
     * 1. sys_user (用户表) - 逻辑删除
     * 理由:
     * - 医疗行业法规要求数据可追溯
     * - 护士删除后，她护理过的病人记录仍需保留关联
     * - 防止误删，支持数据恢复
     * - 审计需要
     */
    
    /**
     * 2. sys_patient (病人表) - 逻辑删除
     * 理由:
     * - 医疗病历需要长期保存
     * - 法律规定的医疗数据追溯期
     * - 病人转院或出院后仍需保留记录
     */
    
    /**
     * 3. sys_nursing_record (护理记录表) - 逻辑删除
     * 理由:
     * - 医疗责任追溯
     * - 护理质量评估
     * - 医疗纠纷证据
     */
    
    /**
     * 4. sys_dept (部门表) - 逻辑删除
     * 理由:
     * - 组织架构调整历史
     * - 历史数据关联
     */
    
    /**
     * 5. sys_dict_data (字典数据表) - 逻辑删除
     * 理由:
     * - 基础配置数据
     * - 历史数据引用
     */
    
    // ==================== 物理删除的表 (不使用 @TableLogic) ====================
    
    /**
     * 1. sys_operation_log (操作日志表) - 物理删除
     * 理由:
     * - 数据量大，避免表膨胀
     * - 定期清理 (如保留 6 个月)
     * - 过期数据无保留价值
     * 实现方式:
     * - 定时任务每天清理旧日志
     * - OperationLogServiceImpl.scheduledCleanOldLogs()
     */
    
    /**
     * 2. sys_login_log (登录日志表) - 物理删除
     * 理由:
     * - 数据量大
     * - 安全审计只需保留近期记录
     * - 定期清理 (如保留 3 个月)
     */
    
    /**
     * 3. sys_verify_code (验证码表) - 物理删除
     * 理由:
     * - 临时数据
     * - 过期即失效
     * - 验证成功后即可删除
     */
    
    /**
     * 4. sys_temp_data (临时数据表) - 物理删除
     * 理由:
     * - 临时缓存数据
     * - 定期清理
     */
    
    /**
     * 5. sys_job (定时任务表) - 物理删除
     * 理由:
     * - 配置数据
     * - 删除即表示不再需要
     */
    
    // ==================== 特殊处理的表 ====================
    
    /**
     * 1. sys_user_role (用户角色关联表) - 物理删除
     * 理由:
     * - 关联数据，随用户删除而删除
     * - 数据量小
     * 实现方式:
     * - 删除用户时同步删除关联
     * - UserRoleMapper.deleteUserRoleByUserId()
     */
    
    /**
     * 2. sys_role_menu (角色菜单关联表) - 物理删除
     * 理由:
     * - 关联数据，随角色删除而删除
     * - 数据量小
     * 实现方式:
     * - 删除角色时同步删除关联
     * - RoleMapper.deleteRoleMenuByRoleId()
     */
    
    /**
     * 3. sys_role (角色表) - 物理删除
     * 理由:
     * - 配置数据量小
     * - 删除即表示不再需要
     * - 避免脏数据残留
     * 注意:
     * - 删除前检查是否有关联用户
     * - 提示用户先转移或解绑
     */
    
    /**
     * 4. sys_menu (菜单表) - 物理删除
     * 理由:
     * - 配置数据量小
     * - 删除即表示不再需要
     * 注意:
     * - 删除前检查是否有关联角色
     */
    
    // ==================== 物理删除实现方式 ====================
    
    /**
     * 方式 1: 自定义 SQL
     * 在 Mapper 中定义:
     * @Delete("DELETE FROM sys_user WHERE user_id = #{userId}")
     * int physicallyDeleteById(@Param("userId") Long userId);
     */
    
    /**
     * 方式 2: 使用 MyBatis-Plus 的 baseMapper.delete()
     * 注意：表不能有 @TableLogic 注解，否则会转为逻辑删除
     */
    
    /**
     * 方式 3: 在 Service 层提供专门方法
     * 如：UserServiceImpl.physicallyDeleteUser()
     * 用于 GDPR 合规场景 (用户注销账号)
     */
    
    // ==================== 最佳实践建议 ====================
    
    /**
     * 1. 默认使用逻辑删除，除非有明确理由使用物理删除
     * 2. 对于日志类表，实现定时清理任务
     * 3. 对于关联表，使用级联删除
     * 4. 对于配置表，删除前检查依赖关系
     * 5. 提供数据恢复功能 (针对逻辑删除)
     * 6. 记录删除日志 (谁在什么时候删除了什么)
     * 7. GDPR 合规：提供用户数据导出和彻底删除功能
     */
}
